    public void cargarDatosEnTabla(List<Metadatos> listaMetadatos, JTable tabla) {
    DefaultTableModel modeloTabla = new DefaultTableModel();
    modeloTabla.addColumn("UUID");
    modeloTabla.addColumn("RFC Emisor");
    modeloTabla.addColumn("Nombre Emisor");
    modeloTabla.addColumn("RFC Receptor");
    modeloTabla.addColumn("Nombre Receptor");
    modeloTabla.addColumn("RFC PAC");
    modeloTabla.addColumn("Fecha Emisi√≥n");
    modeloTabla.addColumn("Fecha Certificaci√≥n SAT");
    modeloTabla.addColumn("Monto");
    modeloTabla.addColumn("Efecto Comprobante");
    modeloTabla.addColumn("Estatus");
    modeloTabla.addColumn("Fecha Cancelaci√≥n");

    for (Metadatos metadato : listaMetadatos) {
        Object[] fila = {
            metadato.getUuid(),
            metadato.getRfcEmisor(),
            metadato.getNombreEmisor(),
            metadato.getRfcReceptor(),
            metadato.getNombreReceptor(),
            metadato.getRfcPac(),
            metadato.getFechaEmision(),
            metadato.getFechaCertificacionSat(),
            metadato.getMonto(),
            metadato.getEfectoComprobante(),
            metadato.getEstatus(),
            metadato.getFechaCancelacion()
        };
        modeloTabla.addRow(fila);
    }

    tabla.setModel(modeloTabla);
}

