    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int resultado = chooser.showOpenDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            // Aqu√≠ puedes obtener el archivo seleccionado
            java.io.File archivoSeleccionado = chooser.getSelectedFile();
            // Haz algo con el archivo seleccionado
            jTextField1.setText(archivoSeleccionado.getAbsolutePath());
            MetadatosService metadatos = new MetadatosService();
        _metadatosLista = metadatos.leerArchivo(jTextField1.getText());
        if(_metadatosLista != null){
            cargarDatosEnTabla(_metadatosLista,jTable1);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Archivo sin informacion o con datos incorrectos.");
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

