    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(_metadatosLista == null){
            JOptionPane.showMessageDialog(this, "No hay informacion por guardar, favor de primero seleccionar un archivo de metadatos.");
        }
        else
        {
            MetadatosService metadatos = new MetadatosService();
            if(metadatos.registrarMetadatos(_metadatosLista)){
                 JOptionPane.showMessageDialog(this, "Informacion importada existosamente.");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "La importacion no se realizo.");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

